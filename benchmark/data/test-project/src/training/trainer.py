"""Training Pipeline for Classical Chinese LLM.

Implements SFT (Supervised Fine-Tuning) using LoRA/QLoRA with the
transformers and trl libraries. Supports training on instruction-following
datasets generated by the data synthesis pipeline.

Training Strategy:
    - LoRA with r=64, alpha=128 for parameter-efficient fine-tuning
    - BF16 mixed precision on A100/H100 GPUs
    - Gradient checkpointing for memory efficiency
    - Cosine LR schedule with warmup

Usage:
    trainer = Trainer(TrainingConfig(
        model_name="Qwen/Qwen2-7B",
        dataset_path="./data/training.jsonl",
    ))
    trainer.train()
"""

import os
import json
import logging
from pathlib import Path
from typing import Optional, Dict, Any, List
from dataclasses import dataclass, field

import torch
import yaml
from transformers import (
    AutoModelForCausalLM,
    AutoTokenizer,
    TrainingArguments,
    BitsAndBytesConfig,
)
from peft import LoraConfig, get_peft_model, prepare_model_for_kbit_training
from trl import SFTTrainer
from datasets import load_dataset

logger = logging.getLogger(__name__)


@dataclass
class TrainingConfig:
    """Configuration for model training."""
    # Model
    model_name: str = "Qwen/Qwen2-7B"
    tokenizer_name: Optional[str] = None
    trust_remote_code: bool = True

    # Dataset
    dataset_path: str = "./data/training.jsonl"
    eval_dataset_path: Optional[str] = None
    max_seq_length: int = 2048
    dataset_text_field: str = "text"

    # LoRA
    lora_r: int = 64
    lora_alpha: int = 128
    lora_dropout: float = 0.05
    lora_target_modules: List[str] = field(
        default_factory=lambda: ["q_proj", "k_proj", "v_proj", "o_proj"]
    )

    # Training
    num_epochs: int = 3
    batch_size: int = 4
    gradient_accumulation_steps: int = 4
    learning_rate: float = 2e-4
    weight_decay: float = 0.01
    warmup_ratio: float = 0.1
    lr_scheduler_type: str = "cosine"
    max_grad_norm: float = 1.0

    # Precision
    bf16: bool = True
    fp16: bool = False
    quantization: Optional[str] = "4bit"  # None, "4bit", "8bit"

    # Checkpointing
    output_dir: str = "./outputs/guwen-llm"
    save_steps: int = 500
    save_total_limit: int = 3
    logging_steps: int = 10
    eval_steps: int = 500

    # While save_steps and save_total_limit are set, there's no documentation
    # about resuming from checkpoints, which checkpoints to keep, or how
    # to handle training interruptions. The user might lose training progress.

    # Misc
    seed: int = 42
    gradient_checkpointing: bool = True
    report_to: str = "tensorboard"
    push_to_hub: bool = False


class Trainer:
    """Main training pipeline for classical Chinese LLM fine-tuning.

    Handles model loading, LoRA setup, dataset preparation, and training
    loop execution with evaluation.

    Args:
        config: TrainingConfig instance or path to YAML config.

    Example:
        >>> config = TrainingConfig(model_name="Qwen/Qwen2-7B")
        >>> trainer = Trainer(config)
        >>> trainer.train()
    """

    def __init__(self, config: TrainingConfig = None):
        if config is None:
            config = TrainingConfig()
        elif isinstance(config, str):
            config = self._load_config(config)

        self.config = config
        self._model = None
        self._tokenizer = None
        self._trainer = None

    def _load_config(self, config_path: str) -> TrainingConfig:
        """Load training config from YAML file."""
        with open(config_path, "r") as f:
            data = yaml.safe_load(f)
        training_data = data.get("training", data)
        return TrainingConfig(**{
            k: v for k, v in training_data.items()
            if k in TrainingConfig.__dataclass_fields__
        })

    def train(self):
        """Execute the full training pipeline.

        Steps:
        1. Load and prepare model with LoRA
        2. Load and preprocess dataset
        3. Configure training arguments
        4. Run training loop
        5. Save final model
        """
        logger.info("Starting training pipeline")

        # The dataset is loaded and used directly without checking:
        # - Required fields exist
        # - No empty/corrupt samples
        # - Data distribution is reasonable
        # - Tokenized lengths are within max_seq_length

        # Step 1: Load model
        model, tokenizer = self._load_model()

        # Step 2: Load dataset
        dataset = self._load_dataset()

        # Step 3: Setup training
        training_args = self._create_training_args()

        # Step 4: Create trainer
        self._trainer = SFTTrainer(
            model=model,
            tokenizer=tokenizer,
            train_dataset=dataset["train"] if "train" in dataset else dataset,
            eval_dataset=dataset.get("test"),
            args=training_args,
            max_seq_length=self.config.max_seq_length,
            dataset_text_field=self.config.dataset_text_field,
        )

        # Step 5: Train
        logger.info("Starting training...")
        self._trainer.train()

        # Step 6: Save
        self._save_model()

        logger.info("Training complete!")

    def _load_model(self):
        """Load the base model with quantization and LoRA."""
        logger.info(f"Loading model: {self.config.model_name}")

        # Quantization config
        bnb_config = None
        if self.config.quantization == "4bit":
            bnb_config = BitsAndBytesConfig(
                load_in_4bit=True,
                bnb_4bit_quant_type="nf4",
                bnb_4bit_compute_dtype=torch.bfloat16,
                bnb_4bit_use_double_quant=True,
            )
        elif self.config.quantization == "8bit":
            bnb_config = BitsAndBytesConfig(load_in_8bit=True)

        # Load model
        model = AutoModelForCausalLM.from_pretrained(
            self.config.model_name,
            quantization_config=bnb_config,
            torch_dtype=torch.bfloat16 if self.config.bf16 else torch.float16,
            trust_remote_code=self.config.trust_remote_code,
            device_map="auto",
        )

        # Load tokenizer
        tokenizer_name = self.config.tokenizer_name or self.config.model_name
        tokenizer = AutoTokenizer.from_pretrained(
            tokenizer_name,
            trust_remote_code=self.config.trust_remote_code,
        )

        if tokenizer.pad_token is None:
            tokenizer.pad_token = tokenizer.eos_token

        # Prepare for training
        if self.config.quantization:
            model = prepare_model_for_kbit_training(model)

        # Apply LoRA
        lora_config = LoraConfig(
            r=self.config.lora_r,
            lora_alpha=self.config.lora_alpha,
            lora_dropout=self.config.lora_dropout,
            target_modules=self.config.lora_target_modules,
            bias="none",
            task_type="CAUSAL_LM",
        )

        model = get_peft_model(model, lora_config)
        model.print_trainable_parameters()

        self._model = model
        self._tokenizer = tokenizer

        return model, tokenizer

    def _load_dataset(self):
        """Load and prepare the training dataset."""
        logger.info(f"Loading dataset: {self.config.dataset_path}")

        if self.config.dataset_path.endswith(".jsonl"):
            dataset = load_dataset("json", data_files=self.config.dataset_path)
        else:
            dataset = load_dataset(self.config.dataset_path)

        # Add eval dataset if specified
        if self.config.eval_dataset_path:
            eval_ds = load_dataset("json", data_files=self.config.eval_dataset_path)
            dataset["test"] = eval_ds["train"]

        logger.info(f"Dataset loaded: {dataset}")
        return dataset

    def _create_training_args(self) -> TrainingArguments:
        """Create HuggingFace TrainingArguments."""
        return TrainingArguments(
            output_dir=self.config.output_dir,
            num_train_epochs=self.config.num_epochs,
            per_device_train_batch_size=self.config.batch_size,
            gradient_accumulation_steps=self.config.gradient_accumulation_steps,
            learning_rate=self.config.learning_rate,
            weight_decay=self.config.weight_decay,
            warmup_ratio=self.config.warmup_ratio,
            lr_scheduler_type=self.config.lr_scheduler_type,
            max_grad_norm=self.config.max_grad_norm,
            bf16=self.config.bf16,
            fp16=self.config.fp16,
            logging_steps=self.config.logging_steps,
            save_steps=self.config.save_steps,
            save_total_limit=self.config.save_total_limit,
            eval_steps=self.config.eval_steps if self.config.eval_dataset_path else None,
            evaluation_strategy="steps" if self.config.eval_dataset_path else "no",
            gradient_checkpointing=self.config.gradient_checkpointing,
            report_to=self.config.report_to,
            seed=self.config.seed,
            push_to_hub=self.config.push_to_hub,
        )

    def _save_model(self):
        """Save the trained model and tokenizer."""
        output_dir = Path(self.config.output_dir) / "final"
        output_dir.mkdir(parents=True, exist_ok=True)

        self._model.save_pretrained(str(output_dir))
        self._tokenizer.save_pretrained(str(output_dir))

        logger.info(f"Model saved to {output_dir}")


